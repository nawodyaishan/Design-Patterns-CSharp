namespace FacadeMethod;

public class Dvd
{
    public void TurnOnDvd()
    {
        Console.WriteLine("Turning on DVD Player\n");
    }

    void TurnInDvd()
    {
        Console.WriteLine("DVD Disc Tray is Now Open.. Please Enter the DVD\n");
    }

    public void PlayMovie()
    {
        Console.WriteLine("Now Playing Netflix Latest Hits\n");
    }

    public void TurnOffDvd()
    {
        Console.WriteLine("Turning off DVD Player\n");
    }
}